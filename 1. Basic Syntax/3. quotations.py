# 3. Quotations in Python

# Python accepts single ('), double (") and triple (''' or """) quotes to denote string literals, as long as the same type of quote starts and ends the string.'''

# Single Quotes:
print('Using Single Quotes')

# Double Quotes: 
print("Using Double Quotes")

# Multi-line Sentence:
print('''This sentence requires
more than one line''')

# Quotes within Quotes:
print("He said, 'Hey, There!' ") #uses "" for printing and '' for quoting the sentence Hey, There!
print('She replied, "Hi!" ')

# We can use different set of quotes to add a quote said by someone within another set of quotes:
# e.g. use "" for printing and '' for quoting or vice versa